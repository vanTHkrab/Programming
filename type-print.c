# include <stdio.h>

int main() {
    char grade1 = 'A';
    char grade2 = 'B';
    printf("my grade is %c and %c\n", grade1, grade2);
    printf("hello, world!\n and\t you are my friend \"maybe\"\n");
    printf("you are 100%% for me\n ");
    return 0;
}

