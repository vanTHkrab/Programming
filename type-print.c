//  \t = tab
//  \" = double quote "
//  \' = single quote '
//  \\ = backslash \
//  %% = percent sign %
//  \b = backspace
//  \r = carriage return
//  \f = form feed
//  \v = vertical tab
//  \a = alert (bell)
//  \0 = null character
//  \? = question mark ?
//  \ooo = octal number
//  \xhh = hexadecimal number
